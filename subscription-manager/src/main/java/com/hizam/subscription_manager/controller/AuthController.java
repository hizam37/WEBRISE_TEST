package com.hizam.subscription_manager.controller;


import com.hizam.subscription_manager.dto.RegistrationDto;
import com.hizam.subscription_manager.dto.SignInDto;
import com.hizam.subscription_manager.service.AuthenticationService;
import jakarta.servlet.http.HttpServletResponse;
import lombok.RequiredArgsConstructor;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;
@RequestMapping("/api/v1/auth")
@RestController
@RequiredArgsConstructor
public class AuthController {
    private final AuthenticationService authService;


    @PostMapping("/register")
    public void signOut(@RequestBody RegistrationDto registrationDto)
    {
     authService.register(registrationDto);
    }


    @PostMapping("/login")
    public void login(@RequestBody SignInDto authenticationDto, HttpServletResponse response)
    {
        authService.login(authenticationDto,response);
    }





































































































































































































































































































}
